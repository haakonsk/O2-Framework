package O2::Dispatch::ModPerlGlobals::Session;

use strict;

use Tie::Scalar;
our @ISA = qw(Tie::StdScalar);

#------------------------------------------------------------------------------------------------------------
sub TIESCALAR {
  my ($className) = @_;
  return bless {}, $className;
}
#------------------------------------------------------------------------------------------------------------
sub FETCH {
  my ($obj) = @_;
  return $obj->{ $ENV{O2REQUESTID} };
}
#------------------------------------------------------------------------------------------------------------
sub STORE {
  my ($obj, $session) = @_;
  $obj->{ $ENV{O2REQUESTID} } = $session;
}
#------------------------------------------------------------------------------------------------------------
sub DESTROY {
  my ($obj) = @_;
}
#------------------------------------------------------------------------------------------------------------
1;
